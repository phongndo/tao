pub fn run(args: Vec<String>) -> std::process::ExitCode {
    match run_result(args, OutputMode::Human) {
        Ok(()) => std::process::ExitCode::SUCCESS,
        Err(error) => {
            let style = Style::stderr();
            eprintln!("{}", style.red(format!("tao: {error}")));
            std::process::ExitCode::FAILURE
        }
    }
}

pub fn run_for_shell_cd(args: Vec<String>) -> std::process::ExitCode {
    match run_result(args, OutputMode::ShellCd) {
        Ok(()) => std::process::ExitCode::SUCCESS,
        Err(error) => {
            let style = Style::stderr();
            eprintln!("{}", style.red(format!("tao: {error}")));
            std::process::ExitCode::FAILURE
        }
    }
}

pub fn print_help() {
    println!(
        "tao commands\n\nUSAGE:\n  tao new [branch] [--from <ref>] [--print-path] [--json]\n  tao cd [branch] [--print-path]\n  tao local [--print-path]\n  tao handoff [--print-path]\n  tao ls [--json] [--names]\n  tao path [branch]\n  tao rm [branch] [--force] [--yes] [--delete-branch]\n  tao prune\n\nNOTES:\n  taod is the source of truth for workspace/worktree metadata and git operations.\n  Branch names are user-facing. Worktree folders are generated by taod under ~/.tao/worktrees.\n  Handoff moves the current clean branch between a managed worktree and local checkout.\n  A CLI cannot change the parent shell directory by itself; run `eval \"$(tao init zsh)\"`\n  or `tao init zsh --install` to enable auto-cd."
    );
}

pub fn print_worktree_names_for_completion() {
    let Ok(bridge) = TaodBridge::from_default_socket() else {
        return;
    };
    let Ok(workspaces) = list_workspaces(&bridge) else {
        return;
    };
    let Ok(cwd) = std::env::current_dir().and_then(|path| path.canonicalize()) else {
        return;
    };
    let Some(workspace) = best_workspace_match(&workspaces, &cwd) else {
        return;
    };
    let Ok(worktrees) = list_worktrees(&bridge, &workspace.id) else {
        return;
    };

    for worktree in worktrees {
        println!("{}", worktree.branch);
    }
}

#[derive(Clone, Copy, PartialEq, Eq)]
enum OutputMode {
    Human,
    ShellCd,
}

struct Style {
    enabled: bool,
}

impl Style {
    fn stdout() -> Self {
        Self::new(io::stdout().is_terminal())
    }

    fn stderr() -> Self {
        Self::new(io::stderr().is_terminal())
    }

    fn new(is_terminal: bool) -> Self {
        Self {
            enabled: is_terminal && std::env::var_os("NO_COLOR").is_none(),
        }
    }

    fn green(&self, value: impl AsRef<str>) -> String {
        self.paint("32", value.as_ref())
    }

    fn yellow(&self, value: impl AsRef<str>) -> String {
        self.paint("33", value.as_ref())
    }

    fn red(&self, value: impl AsRef<str>) -> String {
        self.paint("31", value.as_ref())
    }

    fn dim(&self, value: impl AsRef<str>) -> String {
        self.paint("2", value.as_ref())
    }

    fn paint(&self, code: &str, value: &str) -> String {
        if self.enabled {
            format!("\x1b[{code}m{value}\x1b[0m")
        } else {
            value.to_string()
        }
    }
}

fn run_result(args: Vec<String>, output_mode: OutputMode) -> Result<(), String> {
    let Some(command) = args.first().map(String::as_str) else {
        print_help();
        return Ok(());
    };

    match command {
        "new" => new_worktree(&args[1..], output_mode),
        "ls" => list_command(&args[1..]),
        "cd" => cd_command(&args[1..], output_mode),
        "local" => local_command(&args[1..], output_mode),
        "handoff" => handoff_command(&args[1..], output_mode),
        "path" => path_command(&args[1..]),
        "rm" => remove_command(&args[1..]),
        "prune" => refresh_command(),
        "help" | "--help" | "-h" => {
            print_help();
            Ok(())
        }
        other => Err(format!("unknown worktree command: {other}")),
    }
}

fn new_worktree(args: &[String], output_mode: OutputMode) -> Result<(), String> {
    let mut branch: Option<String> = None;
    let mut from_ref: Option<String> = None;
    let mut print_path = false;
    let mut json_output = false;

    let mut index = 0;
    while index < args.len() {
        match args[index].as_str() {
            "--from" => {
                index += 1;
                let value = args
                    .get(index)
                    .ok_or_else(|| "--from requires a ref".to_string())?;
                from_ref = Some(value.clone());
            }
            "--print-path" => print_path = true,
            "--json" => json_output = true,
            "--help" | "-h" => {
                println!(
                    "USAGE:\n  tao new [branch] [--from <ref>] [--print-path] [--json]\n\nCreates a taod-managed git worktree. The optional positional argument is the git\nbranch name; when omitted taod generates a branch name. The folder path is\nalways generated by taod."
                );
                return Ok(());
            }
            value if value.starts_with('-') => {
                return Err(format!("unknown option for new: {value}"));
            }
            value => {
                if branch.is_some() {
                    return Err("new accepts at most one branch name".to_string());
                }
                branch = Some(value.to_string());
            }
        }
        index += 1;
    }

    let bridge = connected_bridge()?;
    let workspace = current_workspace(&bridge)?;
    let mut request = json!({
        "type": "worktree.create",
        "id": request_id("worktree-create"),
        "workspaceId": workspace.id,
    });
    if let Some(branch) = &branch {
        request["branch"] = json!(branch);
        request["title"] = json!(branch);
        request["targetBranch"] = json!(branch);
    }
    if let Some(from_ref) = &from_ref {
        request["baseBranch"] = json!(from_ref);
        request["startPoint"] = json!(from_ref);
    }

    let response = bridge.request_value(&request)?;
    ensure_ok(&response)?;
    let worktree = worktree_from_response(&response)?;

    if output_mode == OutputMode::ShellCd {
        eprint_created_worktree(&worktree);
        println!("{}", worktree.path.display());
    } else if json_output {
        println!("{}", selected_worktree_json(&worktree));
    } else if print_path {
        println!("{}", worktree.path.display());
    } else {
        print_created_worktree(&worktree);
    }

    Ok(())
}

fn list_command(args: &[String]) -> Result<(), String> {
    let mut json_output = false;
    let mut names = false;
    for arg in args {
        match arg.as_str() {
            "--json" => json_output = true,
            "--names" => names = true,
            "--help" | "-h" => {
                println!("USAGE:\n  tao ls [--json] [--names]");
                return Ok(());
            }
            value => return Err(format!("unknown option for ls: {value}")),
        }
    }

    let bridge = connected_bridge()?;
    let workspace = current_workspace(&bridge)?;
    let worktrees = list_worktrees(&bridge, &workspace.id)?;
    if names {
        for worktree in worktrees {
            println!("{}", worktree.branch);
        }
    } else if json_output {
        print_worktrees_json(&worktrees);
    } else {
        print_worktree_table(&worktrees);
    }
    Ok(())
}

fn cd_command(args: &[String], output_mode: OutputMode) -> Result<(), String> {
    let mut branch: Option<String> = None;
    let mut print_path = false;
    for arg in args {
        match arg.as_str() {
            "--print-path" => print_path = true,
            "--help" | "-h" => {
                println!(
                    "USAGE:\n  tao cd [branch] [--print-path]\n\nEnters a worktree by branch name only. With no branch, Tao uses the current\nlocal git branch."
                );
                return Ok(());
            }
            value if value.starts_with('-') => {
                return Err(format!("unknown option for cd: {value}"));
            }
            value => {
                if branch.is_some() {
                    return Err("cd accepts at most one branch".to_string());
                }
                branch = Some(value.to_string());
            }
        }
    }

    let bridge = connected_bridge()?;
    let workspace = current_workspace(&bridge)?;
    let branch = match branch {
        Some(branch) => branch,
        None => current_git_branch()?,
    };

    let worktrees = list_worktrees(&bridge, &workspace.id)?;
    let worktree = resolve_worktree_branch(&branch, &worktrees)?;
    if output_mode == OutputMode::ShellCd {
        eprint_selected_worktree(&worktree);
        println!("{}", worktree.path.display());
    } else if print_path {
        println!("{}", worktree.path.display());
    } else {
        print_selected_worktree(&worktree);
    }
    Ok(())
}

