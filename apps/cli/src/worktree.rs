fn write_worktree_table(output: &mut impl Write, worktrees: &[Worktree]) -> io::Result<()> {
    let style = Style::new(io::stdout().is_terminal() || io::stderr().is_terminal());
    let branch_width = worktrees
        .iter()
        .map(|worktree| worktree.branch.len())
        .chain(std::iter::once("branch".len()))
        .max()
        .unwrap_or("branch".len());
    let state_width = worktrees
        .iter()
        .map(|worktree| worktree.state.len())
        .chain(std::iter::once("state".len()))
        .max()
        .unwrap_or("state".len());

    writeln!(
        output,
        "{}",
        style.green(format!(
            "{} worktree{}",
            worktrees.len(),
            if worktrees.len() == 1 { "" } else { "s" }
        ))
    )?;
    writeln!(output)?;

    let current = std::env::current_dir().ok();
    let current_index = current.as_deref().and_then(|cwd| {
        worktrees
            .iter()
            .position(|worktree| path_contains(&worktree.path, cwd))
    });

    for (index, worktree) in worktrees.iter().enumerate() {
        let marker = if Some(index) == current_index {
            style.green("●")
        } else {
            style.dim("○")
        };
        writeln!(
            output,
            "{}  {:<branch_width$}  {:<state_width$}  {}",
            marker,
            worktree.branch,
            state_label(&style, &worktree.state),
            style.dim(compact_path(&worktree.path))
        )?;
    }
    Ok(())
}

fn state_label(style: &Style, state: &str) -> String {
    match state {
        "active" => style.green(state),
        "error" => style.red(state),
        _ => style.yellow(state),
    }
}

fn compact_path(path: &Path) -> String {
    let path_text = path.to_string_lossy();
    if let Some(home) = std::env::var_os("HOME").filter(|value| !value.is_empty()) {
        let home = PathBuf::from(home);
        if let Ok(stripped) = path.strip_prefix(&home) {
            return format!("~/{}", stripped.display());
        }
    }
    path_text.to_string()
}

