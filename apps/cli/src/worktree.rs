fn choose_worktree(
    bridge: &TaodBridge,
    workspace_id: &str,
    query: Option<&str>,
) -> Result<Worktree, String> {
    let worktrees = list_worktrees(bridge, workspace_id)?;
    if let Some(query) = query {
        return resolve_worktree(query, &worktrees);
    }
    if worktrees.is_empty() {
        return Err("no taod-managed worktrees found for this workspace".to_string());
    }
    if io::stdin().is_terminal() && command_exists("fzf") {
        return choose_worktree_with_fzf(&worktrees);
    }
    if worktrees.len() == 1 {
        return Ok(worktrees[0].clone());
    }
    let style = Style::stderr();
    eprintln!(
        "{}",
        style.yellow("! Multiple worktrees found; provide a query or install fzf")
    );
    eprintln!();
    eprint_worktree_table(&worktrees);
    Err("provide a query, or install fzf for interactive selection".to_string())
}

fn resolve_worktree(query: &str, worktrees: &[Worktree]) -> Result<Worktree, String> {
    let mut stderr = io::stderr();
    resolve_worktree_with_diagnostics(query, worktrees, &mut stderr)
}

fn resolve_worktree_with_diagnostics(
    query: &str,
    worktrees: &[Worktree],
    diagnostics: &mut impl Write,
) -> Result<Worktree, String> {
    let mut exact_matches = Vec::new();
    let mut partial_matches = Vec::new();
    for worktree in worktrees {
        let path = worktree.path.to_string_lossy();
        let is_exact = worktree.id == query
            || worktree.branch == query
            || worktree.folder_name == query
            || path == query;
        let is_partial = worktree.branch.contains(query)
            || worktree.folder_name.contains(query)
            || path.contains(query);

        if is_exact {
            exact_matches.push(worktree.clone());
        } else if is_partial {
            partial_matches.push(worktree.clone());
        }
    }

    let mut matches = if exact_matches.is_empty() {
        partial_matches
    } else {
        exact_matches
    };

    match matches.len() {
        0 => Err(format!("no worktree matches {query:?}")),
        1 => Ok(matches.remove(0)),
        _ => {
            let style = Style::stderr();
            let _ = writeln!(
                diagnostics,
                "{}",
                style.yellow(format!("! Query {query:?} matched multiple worktrees"))
            );
            let _ = writeln!(diagnostics);
            let _ = write_worktree_table(diagnostics, &matches);
            let _ = writeln!(diagnostics, "\nTry an exact branch, folder, id, or path.");
            Err(format!("query {query:?} matched multiple worktrees"))
        }
    }
}

fn resolve_worktree_branch(branch: &str, worktrees: &[Worktree]) -> Result<Worktree, String> {
    let mut matches: Vec<Worktree> = worktrees
        .iter()
        .filter(|worktree| worktree.branch == branch)
        .cloned()
        .collect();

    match matches.len() {
        0 => Err(format!("no worktree matches branch {branch:?}")),
        1 => Ok(matches.remove(0)),
        _ => {
            let style = Style::stderr();
            eprintln!(
                "{}",
                style.yellow(format!("! Branch {branch:?} matched multiple worktrees"))
            );
            eprintln!();
            eprint_worktree_table(&matches);
            Err(format!("branch {branch:?} matched multiple worktrees"))
        }
    }
}

fn choose_worktree_with_fzf(worktrees: &[Worktree]) -> Result<Worktree, String> {
    let mut input = String::new();
    for (index, worktree) in worktrees.iter().enumerate() {
        writeln!(
            input,
            "{}\t{}\t{}\t{}",
            index,
            worktree.branch,
            worktree.state,
            worktree.path.display()
        )
        .map_err(|error| error.to_string())?;
    }

    let mut child = Command::new("fzf")
        .arg("--delimiter")
        .arg("\t")
        .arg("--with-nth")
        .arg("2,3,4")
        .arg("--prompt")
        .arg("tao> ")
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .spawn()
        .map_err(|error| format!("failed to start fzf: {error}"))?;

    if let Some(stdin) = child.stdin.as_mut() {
        stdin
            .write_all(input.as_bytes())
            .map_err(|error| format!("failed to write fzf choices: {error}"))?;
    }

    let output = child
        .wait_with_output()
        .map_err(|error| format!("failed to wait for fzf: {error}"))?;
    if !output.status.success() {
        return Err("no worktree selected".to_string());
    }

    let selection = String::from_utf8(output.stdout)
        .map_err(|error| format!("fzf returned non-utf8 output: {error}"))?;
    let index = selection
        .split('\t')
        .next()
        .ok_or_else(|| "fzf returned an empty selection".to_string())?
        .trim()
        .parse::<usize>()
        .map_err(|error| format!("fzf returned an invalid selection: {error}"))?;
    worktrees
        .get(index)
        .cloned()
        .ok_or_else(|| "fzf selected an unknown worktree".to_string())
}

